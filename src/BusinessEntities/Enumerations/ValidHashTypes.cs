namespace AdvancedLogging.Enumerations
{
    /// <summary>
    /// Enumeration representing different types of hash algorithms.
    /// </summary>
    public enum HashType
    {
        /// <summary>
        /// Represents an unsecured hash type.
        /// </summary>
        Unsecured,

        /// <summary>
        /// Represents the SHA-256 hash algorithm.
        /// </summary>
        SHA2_256,

        /// <summary>
        /// Represents the SHA-512 hash algorithm.
        /// </summary>
        SHA2_512
    };
}
